package entites;

public class Department {
	private String name;
	
	
	//Contructors
	public Department() {
		
	}
	
	public Department(String name) {
		super();
		this.name = name;
	}
	
	
	//Getters and Setters
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
